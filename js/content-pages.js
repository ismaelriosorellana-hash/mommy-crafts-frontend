    function createTextBlocks(text) {
        const fragment = document.createDocumentFragment();
        String(text || "")
            .split(/\n{2,}/)
            .map((part) => part.trim())
            .filter(Boolean)
            .forEach((part) => {
                const paragraph = document.createElement("p");
                paragraph.textContent = part;
                fragment.appendChild(paragraph);
            });
        return fragment;
    }

    function safeExternalUrl(value) {
        const url = String(value || "").trim();
        return /^(https?:\/\/|mailto:|tel:)/i.test(url) ? url : "";
    }

    function renderSections(content) {
        const container = document.getElementById("content-sections");
        if (!container) return;
        container.innerHTML = "";

        (content.sections || []).forEach((section, index) => {
            const article = document.createElement("section");
            article.className = "content-section-card";
            article.style.setProperty("--content-index", String(index));

            const number = document.createElement("span");
            number.className = "content-section-number";
            number.textContent = String(index + 1).padStart(2, "0");

            const body = document.createElement("div");
            body.className = "content-section-body";

            const title = document.createElement("h2");
            title.textContent = section.title || "Información";
            body.appendChild(title);
            body.appendChild(createTextBlocks(section.body));

            if (Array.isArray(section.items) && section.items.length) {
                const list = document.createElement("ul");
                section.items.forEach((item) => {
                    const entry = document.createElement("li");
                    entry.textContent = item;
                    list.appendChild(entry);
                });
                body.appendChild(list);
            }

            article.append(number, body);
            container.appendChild(article);
        });

        container.hidden = !container.children.length;
    }

    function renderFaqs(content) {
        const section = document.getElementById("content-faq-section");
        const container = document.getElementById("content-faqs");
        if (!section || !container) return;
        container.innerHTML = "";

        (content.faqs || []).forEach((faq, index) => {
            const detail = document.createElement("details");
            detail.className = "content-faq";
            if (index === 0) detail.open = true;

            const summary = document.createElement("summary");
            summary.textContent = faq.question || "Pregunta";

            const answer = document.createElement("div");
            answer.className = "content-faq-answer";
            answer.appendChild(createTextBlocks(faq.answer));

            detail.append(summary, answer);
            container.appendChild(detail);
        });

        section.hidden = !container.children.length;
    }

    function renderContactCards(content) {
        const section = document.getElementById("content-contact-section");
        const container = document.getElementById("content-contact-cards");
        if (!section || !container) return;
        container.innerHTML = "";

        (content.contactCards || []).forEach((card) => {
            const article = document.createElement("article");
            article.className = "content-contact-card";

            const title = document.createElement("h3");
            title.textContent = card.title || "Contacto";

            const detail = document.createElement("p");
            detail.textContent = card.detail || "";

            article.append(title, detail);

            const url = safeExternalUrl(card.actionUrl);
            if (url && card.actionLabel) {
                const link = document.createElement("a");
                link.className = "content-action-link";
                link.href = url;
                link.textContent = card.actionLabel;
                if (/^https?:/i.test(url)) {
                    link.target = "_blank";
                    link.rel = "noopener noreferrer";
                }
                article.appendChild(link);
            }

            container.appendChild(article);
        });

        section.hidden = !container.children.length;
    }

    function updateMetadata(content) {
        if (content.seoTitle) document.title = content.seoTitle;
        const description = document.querySelector('meta[name="description"]');
        if (description && content.seoDescription) {
            description.setAttribute("content", content.seoDescription);
        }
    }

